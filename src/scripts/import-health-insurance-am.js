async function run() {
  await mongoose.connect(MONGO_URI);

  await Promise.all([
    Interaction.deleteMany({}),
    Alert.deleteMany({}),
    CoachingItem.deleteMany({}),
    Dispute.deleteMany({}),
    AuditLog.deleteMany({}),
  ]);

  const dataset = buildDataset();
  const docs = dataset.map((item, idx) => makeInteraction(item, idx));
  await Interaction.insertMany(docs);

  const totals = await Interaction.aggregate([
    { $unwind: "$aiVersions" },
    { $sort: { "aiVersions.version": -1 } },
    {
      $group: {
        _id: "$interactionId",
        sentiment: { $first: "$aiVersions.ai.sentimentLabel" },
      },
    },
    { $group: { _id: "$sentiment", count: { $sum: 1 } } },
  ]);

  const counts = Object.fromEntries(totals.map((t) => [t._id, t.count]));
  console.log("Imported Armenian health-insurance interactions:");
  console.log(`positive=${counts.positive || 0}, negative=${counts.negative || 0}, neutral=${counts.neutral || 0}`);
  console.log(`total=${docs.length}`);

  await mongoose.disconnect();
}

run().catch(async (err) => {
  console.error("Import failed:", err);
  await mongoose.disconnect();
  process.exit(1);
});
